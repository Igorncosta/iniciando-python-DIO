nome = "Igor Nunes da Costa"

print(nome[0]) # Retorna a primeira Letra na posição 0
print(nome[-2]) # Retorna a penultima letra na posição FINAL -2
print(nome[:4]) # Retorna do INICIO ate a posição 4
print(nome[5:]) # Retorna desde a posicção 5 até o FINAL
print(nome[5:20]) # Retorna desde a posição 5 até a posição 20
print(nome[5:20:2]) #Retorna desde a posição 5 até a 20 de 2 em 2 caracteres
print(nome[:]) # Retorna do INICIO ao FINAL
print(nome[:: -1]) # Inverte todas posições do FINAL ao INICIAL